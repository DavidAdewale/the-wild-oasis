import { cloneElement, createContext, useContext, useState } from 'react';
import { createPortal } from 'react-dom';
import { HiXMark } from 'react-icons/hi2';
import styled from 'styled-components';
import { useOutsideClick } from '../hooks/useOutsideClick';

const StyledModal = styled.div`
  position: fixed;
  top: 50%;
  left: 50%;
  transform: translate(-50%, -50%);
  background-color: var(--color-grey-0);
  border-radius: var(--border-radius-lg);
  box-shadow: var(--shadow-lg);
  padding: 3.2rem 4rem;
  transition: all 0.5s;
`;

const Overlay = styled.div`
  position: fixed;
  top: 0;
  left: 0;
  width: 100%;
  height: 100vh;
  background-color: var(--backdrop-color);
  backdrop-filter: blur(4px);
  z-index: 1000;
  transition: all 0.5s;
`;

const Button = styled.button`
  background: none;
  border: none;
  padding: 0.4rem;
  border-radius: var(--border-radius-sm);
  transform: translateX(0.8rem);
  transition: all 0.2s;
  position: absolute;
  top: 1.2rem;
  right: 1.9rem;

  &:hover {
    background-color: var(--color-grey-100);
  }

  & svg {
    width: 2.4rem;
    height: 2.4rem;
    /* Sometimes we need both */
    /* fill: var(--color-grey-500);
    stroke: var(--color-grey-500); */
    color: var(--color-grey-500);
  }
`;

//This initializes the context API
const ModalContext = createContext();

//The purpose of this is to store the state and provide the state updating functions to its children components
function Modal({ children }) {
  const [openName, setOpenName] = useState('');
  const close = () => setOpenName('');
  const open = setOpenName;
  return (
    <ModalContext.Provider value={{ openName, close, open }}>
      {children}
    </ModalContext.Provider>
  );
}

//The purpose of this is to decide what content the modal opens by passing the 'name' prop to it's child (which is a button that opens the modal)
function Open({ children, opens: opensWindowName }) {
  const { open } = useContext(ModalContext);

  //The child cannot directly be passed this open state updating function so this cloning technique is used to clone the children and add an onclick function where the state updating function can be used to trigger the modal
  return cloneElement(children, { onClick: () => open(opensWindowName) });
}

//This is the modal window, it grabs the name passed in and compares it with the name stored in the state, if it's the same, then it displays the modal but if not, it returns null || It also grabs the close function from the context
function Window({ children, name }) {
  const { openName, close } = useContext(ModalContext);
  const ref = useOutsideClick(close);

  if (name !== openName) return null;

  //This portal ensures that the modal is always a direct child of the body element (document.body) to avoid any issues with styling. Basically, it renders it as a direct child of the body element in the DOM but does not affect it's position in the component tree
  return createPortal(
    <Overlay>
      <StyledModal ref={ref}>
        <Button onClick={close}>
          <HiXMark />
        </Button>
        <div>{cloneElement(children, { onCloseModal: close })}</div>
      </StyledModal>
    </Overlay>,
    document.body
  );
}

//This is how to create Compound Components.. Basically linking the Open and Window functions to the modal
Modal.Open = Open;
Modal.Window = Window;

